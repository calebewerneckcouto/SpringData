package cursojava.executavel;

public class PrimeiraClasseJava {
	
	
	/*Main é um metodo auto executavel em java*/
	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 2;
		int soma= num1+num2;
		int subtracao = num1-num2;
		int multiplicacao = num1*num2;
		int divisao = num1/num2;
		
		
		
		/*Escrever mensagem*/
		System.out.println("A Soma de:"+num1 +" + "+num2 +" é igual á:"+ soma);
		System.out.println("A Subtracão de:"+num1 +" - "+num2 +" é igual á:"+ subtracao);
		System.out.println("A Multiplicação de:"+num1 +" * "+num2 +" é igual á:"+ multiplicacao);
		System.out.println("A Divisão de:"+num1 +" / "+num2 +" é igual á:"+ divisao);
		
		
	}

}
